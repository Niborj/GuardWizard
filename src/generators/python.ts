function pythonTest(c: WizardConfig) {
  return `${header(c)}
"""Smoke test for the Cato API Guard integration.

Run:
    pip install requests python-dotenv
    export ${c.guardKeyEnvVar}=...
    export CATO_TEST_INPUT='<a value your policy detects>'
    python test_guard.py

The wizard intentionally does NOT bundle a sample sensitive value. Use
any value your policy is configured to flag (PII, secret, etc.).
"""

import json
import os
import sys
from cato_guard import CatoGuard, new_session_id


def main() -> None:
    test_input = os.environ.get("CATO_TEST_INPUT")
    if not test_input:
        print(
            "Set CATO_TEST_INPUT to a value your policy detects, e.g.:\\n"
            "  export CATO_TEST_INPUT='<your test value>'",
            file=sys.stderr,
        )
        sys.exit(2)

    guard = CatoGuard()
    session_id = new_session_id()
    decision = guard.inspect(
        [
            {"role": "user", "content": "Hi"},
            {"role": "assistant", "content": "Hi, how can I help you?"},
            {"role": "user", "content": f"Please look up: {test_input}"},
        ],
        session_id=session_id,
    )
    print("Action:    ", decision.action_type)
    print("Policy:    ", decision.policy_name)
    print("Detection: ", decision.detection_message)
    print("Blocked:   ", decision.blocked)
    print("Redacted:  ", decision.redacted)
    print("Raw response:")
    print(json.dumps(decision.raw, indent=2))


if __name__ == "__main__":
    main()
`;
}

function pythonDirectIntegration(c: WizardConfig): string {
  const provider = providerSnippet(c);
  return `${header(c)}
"""Reference integration: wrap an LLM call with Cato guard hooks.

This file shows exactly where to call the guard. Copy the pattern into
the customer's existing application.
"""

import os
from cato_guard import CatoGuard, new_session_id

${provider.imports}

guard = CatoGuard()


def chat(history, user_text, *, session_id=None, user_id=None, conversation_id=None):
    """End-to-end guarded chat turn."""
    session_id = session_id or new_session_id(
        user_id=user_id,
        conversation_id=conversation_id,
    )
    history = list(history)

${
  c.inspectionPoints.includes("system_message")
    ? `    # ─── 0. Conversation setup: inspect system messages ──────────────
    system_check = guard.inspect_system_messages(history, session_id=session_id)
    if system_check.blocked:
        return _refusal(system_check)
    if system_check.redacted:
        history = system_check.messages

`
    : ""
}${
  c.inspectionPoints.includes("user_input")
    ? `    # ─── 1. Pre-LLM: inspect user input ──────────────────────────────
    pre = guard.inspect_user_input(history, user_text, session_id=session_id)
    if pre.blocked:
        return _refusal(pre)
    # If the policy redacted the message, use the cleaned messages going forward.
    history = pre.messages if pre.redacted else history + [{"role": "user", "content": user_text}]
`
    : `    history = history + [{"role": "user", "content": user_text}]
`
}
    # ─── 2. Call the model ───────────────────────────────────────────
${provider.call}

${
  c.inspectionPoints.includes("assistant_output")
    ? `    # ─── 3. Post-LLM: inspect assistant output ───────────────────────
    post = guard.inspect_assistant_output(history, assistant_text, session_id=session_id)
    if post.blocked:
        return _refusal(post)
    if post.redacted:
        # the last redacted message replaces the assistant turn
        assistant_text = post.messages[-1]["content"]
`
    : ""
}
    return assistant_text


def _refusal(decision):
    return (
        "I can't help with that - the request was blocked by the safety guard "
        f"({decision.policy_name or 'policy'}: {decision.detection_message or 'no details'})."
    )


if __name__ == "__main__":
    print(chat([], "Hello!"))
`;
}

