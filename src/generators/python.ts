import type { WizardConfig } from "../types";
import type { GeneratedFile } from "./index";

export function generatePython(c: WizardConfig): {
  files: GeneratedFile[];
  primary: string;
} {
  const files: GeneratedFile[] = [];
  files.push({
    path: "cato_guard.py",
    language: "python",
    description:
      "Reusable Cato API Guard client. Drop this file into your project. All inspection helpers and the failure-mode policy live here.",
    content: pythonClient(c),
  });

  files.push({
    path: ".env.example",
    language: "ini",
    description:
      "Example env file. Set the guard key here, then load via python-dotenv or your secrets manager.",
    content: envFile(c),
  });

  files.push({
    path: "test_guard.py",
    language: "python",
    description:
      "Smoke test against the live Cato endpoint. Set CATO_TEST_INPUT to a value your policy is configured to detect (e.g., a sample PII string, a known-bad pattern).",
    content: pythonTest(c),
  });

  if (c.architecture === "direct-sdk") {
    files.push({
      path: integrationFilename(c),
      language: "python",
      description:
        "Reference integration showing exactly where to wrap your existing LLM call with guard hooks.",
      content: pythonDirectIntegration(c),
    });
  } else if (c.architecture === "langchain") {
    files.push({
      path: "guarded_chat.py",
      language: "python",
      description:
        "LangChain integration: a Runnable wrapper that inspects messages before/after each model call and tool node.",
      content: pythonLangChainIntegration(c),
    });
  } else if (c.architecture === "langgraph") {
    files.push({
      path: "guarded_graph.py",
      language: "python",
      description:
        "LangGraph integration: guard nodes you can splice into your StateGraph between agent and tool nodes.",
      content: pythonLangGraphIntegration(c),
    });
  } else {
    files.push({
      path: integrationFilename(c),
      language: "python",
      description:
        "Generic integration recipe - adapt this skeleton to the customer's specific framework.",
      content: pythonDirectIntegration(c),
    });
  }

  return { files, primary: "cato_guard.py" };
}

function integrationFilename(c: WizardConfig) {
  const map: Record<string, string> = {
    openai: "openai_with_guard.py",
    anthropic: "anthropic_with_guard.py",
    "azure-openai": "azure_openai_with_guard.py",
    bedrock: "bedrock_with_guard.py",
    gemini: "gemini_with_guard.py",
    mistral: "mistral_with_guard.py",
    cohere: "cohere_with_guard.py",
    ollama: "ollama_with_guard.py",
    other: "generic_with_guard.py",
  };
  return map[c.provider] ?? "integration.py";
}

function header(c: WizardConfig) {
  const lines = [
    "# ============================================================",
    `# Cato API Guard integration`,
    c.customerName ? `# Customer: ${c.customerName}` : null,
    `# Stack: Python · ${c.provider} · ${c.architecture}`,
    `# Inspection: ${c.inspectionPoints.join(", ")}`,
    `# Action: ${c.actionMode} · Failure mode: ${c.failureMode}`,
    "# Generated by the Cato API Guard Integration Wizard",
    "# Review with a developer before deploying to production.",
    "# ============================================================",
  ].filter(Boolean);
  return lines.join("\n");
}

function pythonClient(c: WizardConfig): string {
  const failOpen = c.failureMode === "fail_open";
  const respectsPolicy = c.actionMode === "policy_driven";
  return `${header(c)}
"""Cato API Guard Python client.

Public API:
    guard = CatoGuard()                                  # reads ${c.guardKeyEnvVar} from env
    decision = guard.inspect(messages, session_id="...") # call before/after the LLM
    if decision.blocked: ...
    safe_messages = decision.messages                     # redacted if action == redact

Inspection points enabled by your wizard config:
    ${c.inspectionPoints.join(", ")}
"""

from __future__ import annotations

import logging
import json
import os
import uuid
from dataclasses import dataclass, field
from typing import Any, Iterable

import requests

logger = logging.getLogger("cato_guard")

CATO_ENDPOINT = os.environ.get(
    "CATO_API_URL",
    ${JSON.stringify(c.apiBaseUrl)},
)
GUARD_KEY_ENV = ${JSON.stringify(c.guardKeyEnvVar)}
DEFAULT_TIMEOUT_SECONDS = 5.0


class CatoGuardError(Exception):
    """Raised when the guard rejects configuration, auth, or payload shape."""


class CatoGuardUnavailableError(CatoGuardError):
    """Raised for transient network, timeout, rate-limit, or 5xx failures."""


@dataclass
class GuardDecision:
    """Result of a single guard inspection."""

    blocked: bool = False
    redacted: bool = False
    action_type: str = "pass"
    policy_name: str | None = None
    detection_message: str | None = None
    messages: list[dict[str, Any]] = field(default_factory=list)
    raw: dict[str, Any] | None = None

    @property
    def safe_to_forward(self) -> bool:
        return not self.blocked


class CatoGuard:
    """Cato API Guard client.

    Defaults match your wizard configuration:
        action_mode   = ${c.actionMode}
        failure_mode  = ${c.failureMode}
    """

    def __init__(
        self,
        guard_key: str | None = None,
        endpoint: str = CATO_ENDPOINT,
        timeout: float = DEFAULT_TIMEOUT_SECONDS,
        action_mode: str = ${JSON.stringify(c.actionMode)},
        failure_mode: str = ${JSON.stringify(c.failureMode)},
    ) -> None:
        self.guard_key = guard_key or os.environ.get(GUARD_KEY_ENV)
        if not self.guard_key:
            raise RuntimeError(
                f"Cato guard key missing. Set {GUARD_KEY_ENV} in the environment."
            )
        self.endpoint = endpoint
        self.timeout = timeout
        self.action_mode = action_mode
        self.failure_mode = failure_mode

    # ------------------------------------------------------------------
    # Core inspection
    # ------------------------------------------------------------------
    def inspect(
        self,
        messages: Iterable[dict[str, Any]],
        session_id: str | None = None,
    ) -> GuardDecision:
        """Inspect a conversation. Returns a GuardDecision.

        The 'messages' arg must be in OpenAI format: a list of dicts with
        at least 'role' and 'content'. Tool calls / tool results should
        also be passed in this list - Cato understands them natively.
        """
        msgs = list(messages)
        headers = {
            "Content-Type": "application/json",
            "Authorization": f"Bearer {self.guard_key}",
        }
        if session_id:
            headers["x-cato-session-id"] = session_id

        try:
            response = requests.post(
                self.endpoint,
                json={"messages": msgs},
                headers=headers,
                timeout=self.timeout,
            )
        except requests.RequestException as exc:
            return self._handle_unavailable(exc, msgs)

        if response.status_code == 429 or response.status_code >= 500:
            return self._handle_unavailable(
                CatoGuardUnavailableError(
                    f"Cato guard unavailable ({response.status_code}): {response.text[:300]}"
                ),
                msgs,
            )

        if response.status_code >= 400:
            raise CatoGuardError(
                f"Cato guard rejected the request ({response.status_code}): {response.text[:500]}"
            )

        try:
            data = response.json()
        except ValueError as exc:
            raise CatoGuardError("Cato guard returned a non-JSON success response.") from exc

        return self._interpret(data, msgs)

    def _handle_unavailable(self, exc: Exception, msgs: list[dict[str, Any]]) -> GuardDecision:
        logger.warning("Cato guard unavailable: %s", exc)
            ${
              failOpen
                ? `# fail-open: forward original messages
        return GuardDecision(
            action_type="guard_unavailable",
            detection_message=str(exc),
            messages=msgs,
        )`
                : `# fail-closed: block the request
        return GuardDecision(
            blocked=True,
            action_type="guard_unavailable",
            detection_message=str(exc),
            messages=msgs,
        )`
            }

    # ------------------------------------------------------------------
    # Convenience wrappers for each inspection point
    # ------------------------------------------------------------------
${
  c.inspectionPoints.includes("system_message")
    ? `    def inspect_system_messages(self, history, session_id=None):
        original = list(history)
        system_messages = [m for m in original if m.get("role") == "system"]
        if not system_messages:
            return GuardDecision(messages=original)
        decision = self.inspect(system_messages, session_id=session_id)
        if decision.redacted:
            redacted_system = iter(decision.messages)
            decision.messages = [
                next(redacted_system) if m.get("role") == "system" else m
                for m in original
            ]
        else:
            decision.messages = original
        return decision
`
    : ""
}${
  c.inspectionPoints.includes("user_input")
    ? `    def inspect_user_input(self, history, user_text, session_id=None):
        msgs = list(history) + [{"role": "user", "content": user_text}]
        return self.inspect(msgs, session_id=session_id)
`
    : ""
}${
    c.inspectionPoints.includes("assistant_output")
      ? `
    def inspect_assistant_output(self, history, assistant_text, session_id=None):
        msgs = list(history) + [{"role": "assistant", "content": assistant_text}]
        return self.inspect(msgs, session_id=session_id)
`
      : ""
  }${
    c.inspectionPoints.includes("tool_call_args")
      ? `
    def inspect_tool_call(self, history, tool_name, tool_args, session_id=None, tool_call_id=None):
        call_id = tool_call_id or f"call_{uuid.uuid4().hex}"
        arguments = tool_args if isinstance(tool_args, str) else json.dumps(tool_args, sort_keys=True)
        msgs = list(history) + [{
            "role": "assistant",
            "content": None,
            "tool_calls": [{
                "id": call_id,
                "type": "function",
                "function": {
                    "name": tool_name,
                    "arguments": arguments,
                },
            }],
        }]
        return self.inspect(msgs, session_id=session_id)
`
      : ""
  }${
    c.inspectionPoints.includes("tool_result")
      ? `
    def inspect_tool_result(self, history, tool_name, tool_output, session_id=None, tool_call_id=None):
        call_id = tool_call_id or f"call_{uuid.uuid4().hex}"
        msgs = list(history) + [{
            "role": "tool",
            "tool_call_id": call_id,
            "name": tool_name,
            "content": str(tool_output),
        }]
        return self.inspect(msgs, session_id=session_id)
`
      : ""
  }
    # ------------------------------------------------------------------
    # Internals
    # ------------------------------------------------------------------
    def _interpret(self, data: dict, original: list[dict]) -> GuardDecision:
        action = (data.get("required_action") or {}).get("action_type", "pass")
        policy = (data.get("required_action") or {}).get("policy_name")
        detection = (data.get("required_action") or {}).get("detection_message")
        redacted = (data.get("redacted_chat") or {}).get("all_redacted_messages") or []

        decision = GuardDecision(
            action_type=action,
            policy_name=policy,
            detection_message=detection,
            messages=original,
            raw=data,
        )

        ${
          respectsPolicy
            ? `# policy_driven: honor the action returned by Cato
        if action == "block_action":
            decision.blocked = True
        elif action == "redact_action" and redacted:
            decision.redacted = True
            decision.messages = redacted`
            : c.actionMode === "block"
              ? `# block: any non-pass action becomes a hard block
        if action and action != "pass":
            decision.blocked = True`
              : c.actionMode === "redact"
                ? `# redact: replace messages with the Cato-redacted versions when present
        if redacted:
            decision.redacted = True
            decision.messages = redacted`
                : `# log_only: never block or modify, but log the detection
        if action and action != "pass":
            logger.info("Cato detection (log_only): %s - %s", policy, detection)`
        }

        return decision


def new_session_id(
    user_id: str | None = None,
    conversation_id: str | None = None,
    prefix: str = "session",
) -> str:
    """Generate a Cato session id. Strategy: ${c.sessionIdStrategy}."""
    random_id = str(uuid.uuid4())
    ${
      c.sessionIdStrategy === "per_user"
        ? `if user_id:
        return f"user:{user_id}"
    return f"{prefix}:{random_id}"`
        : c.sessionIdStrategy === "per_conversation"
          ? `if conversation_id:
        return f"conversation:{conversation_id}"
    return f"{prefix}:{random_id}"`
          : `return f"{prefix}:{random_id}"`
    }
`;
}

